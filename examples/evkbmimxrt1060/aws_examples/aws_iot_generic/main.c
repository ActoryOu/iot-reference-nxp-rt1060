#define EXAMPLE_PHY_OPS phyksz8081_ops

/* ENET clock frequency. */
#define EXAMPLE_CLOCK_FREQ CLOCK_GetFreq(kCLOCK_IpgClk)

/* Task priorities. */
#define demo_task_PRIORITY ( tskIDLE_PRIORITY + 1 )
